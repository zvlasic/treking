defmodule Treking.Application do
  # See https://hexdocs.pm/elixir/Application.html
  # for more information on OTP Applications
  @moduledoc false

  use Application

  @impl true
  def start(_type, _args) do
    children = [
      TrekingWeb.Telemetry,
      Treking.Repo,
      {DNSCluster, query: Application.get_env(:treking, :dns_cluster_query) || :ignore},
      {Phoenix.PubSub, name: Treking.PubSub},
      # Start the Finch HTTP client for sending emails
      {Finch, name: Treking.Finch},
      # Start a worker by calling: Treking.Worker.start_link(arg)
      # {Treking.Worker, arg},
      # Start to serve requests, typically the last entry
      TrekingWeb.Endpoint
    ]

    # See https://hexdocs.pm/elixir/Supervisor.html
    # for other strategies and supported options
    opts = [strategy: :one_for_one, name: Treking.Supervisor]
    Supervisor.start_link(children, opts)
  end

  # Tell Phoenix to update the endpoint configuration
  # whenever the application is updated.
  @impl true
  def config_change(changed, _new, removed) do
    TrekingWeb.Endpoint.config_change(changed, removed)
    :ok
  end
end
